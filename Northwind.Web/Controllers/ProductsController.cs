namespace Northwind.Web.Controllers
{
    public class ProductsController : Controller
    {
        private readonly ShoppeDBContext _context;

        public ProductsController(ShoppeDBContext context)
        {
            _context = context;
        }

        // GET: Products
        public async Task<IActionResult> Index()
        {
            var shoppeDBContext = _context.Products.Include(p => p.Category);
            return View(await shoppeDBContext.ToListAsync());
        }

        // GET: Products/Details/5
        public async Task<IActionResult> Details(long? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var product = await _context.Products
                .Include(p => p.Category)
                .FirstOrDefaultAsync(m => m.ProductID == id);
            if (product == null)
            {
                return NotFound();
            }

            return View(product);
        }

        // GET: Products/Create
        public IActionResult Create()
        {
            ViewData["CategoryID"] = new SelectList(_context.Categories, "CategoryId", "CategoryId");
            return View();
        }

        // POST: Products/Create
        // To protect from overposting attacks, enable the specific properties you want to bind to.
        // For more details, see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]

        public async Task<IActionResult> Create(ProductDto productDto)
        {
            if (ModelState.IsValid)
            {
                try
                {
                    var file = productDto.Foto;
                    var folderName = Path.Combine("Resources", "Images");
                    var pathToSave = Path.Combine(Directory.GetCurrentDirectory(), folderName);
                    if (file.Length > 0)
                    {
                        var fileName = ContentDispositionHeaderValue.Parse(file.ContentDisposition).FileName.Trim('"');
                        var fullPath = Path.Combine(pathToSave, fileName);
                        var dbPath = Path.Combine(folderName, fileName);
                        using (var stream = new FileStream(fullPath, FileMode.Create))
                        {
                            file.CopyTo(stream);
                        }

                        var product = new Product
                        {
                            Name = productDto.Name,
                            Description = productDto.Description,
                            PhotoImage = fileName,
                            Price = productDto.Price,
                            CategoryID = productDto.CategoryID
                        };
                        //collect data from dto dan filename
                        _context.Add(product);
                        await _context.SaveChangesAsync();

                    }
                }
                catch (Exception ex)
                {

                    throw;
                }
            }
            return View(productDto);
        }


